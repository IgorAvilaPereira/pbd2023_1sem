    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Banner bannerNovo = new Banner();
            bannerNovo.setLink(this.inputLink.getText());
            bannerNovo.setLegenda(this.inputLegenda.getText());
            if (radioSuperior.isSelected()) {
                bannerNovo.setTipo("SUPERIOR");
            } else {
                bannerNovo.setTipo("INFERIOR");
            }
            bannerNovo.setDiretorio(this.dir);
            Empresa empresa = new EmpresaDAO().obter(Integer.parseInt(this.listEmpresa.getSelectedItem().split(";")[0]));
            bannerNovo.setEmpresa(empresa);
            new BannerDAO().inserir(bannerNovo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "erro", "erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FormBanner.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.listRefresh();
    }//GEN-LAST:event_jButton1ActionPerformed

