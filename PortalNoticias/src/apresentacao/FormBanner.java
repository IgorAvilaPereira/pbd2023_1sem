    private void listRefresh() {
        this.list1.removeAll();        
        try {
            ArrayList<Banner> vetBanner = new BannerDAO().listar();
            for (int i = 0; i < vetBanner.size(); i++) {
                Banner b = vetBanner.get(i);
                this.list1.add(b.getId() + ";" + b.getLink(), b.getId());                
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormBanner.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
